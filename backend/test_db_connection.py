            print(f"‚úÖ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —É—Å–ø–µ—à–Ω–æ!")
            print(f"üìä –í–µ—Ä—Å–∏—è PostgreSQL: {version}")
            
